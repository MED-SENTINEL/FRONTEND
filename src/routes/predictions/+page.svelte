<script>
  import { onMount } from 'svelte';
  import { currentUser } from '$lib/stores/auth';
  import { apiFetch } from '$lib/api/client';
  import PredictionsHUD from '$lib/components/dashboard/PredictionsHUD.svelte';
  import { fade, fly } from 'svelte/transition';

  let prediction = null;
  let loading = false;
  let errorMsg = '';

  async function runPrediction() {
    if (!$currentUser) return;
    loading = true;
    errorMsg = '';
    try {
      const results = await apiFetch(`/api/predictions/run/${$currentUser.id}`, { method: 'POST' });
      if (results && results.length > 0) {
          prediction = {
            risk_level: results[0].risk_level,
            confidence: results[0].risk_score,
            recommendation: results[0].explanation?.recommendation || 'Continuous monitoring of vitals shows no immediate intervention required.',
            details: results[0].explanation || {}
          };
      }
    } catch (err) {
      errorMsg = err.message;
    } finally {
      loading = false;
    }
  }

  onMount(runPrediction);
</script>

<div class="space-y-12">
  <div class="flex flex-col md:flex-row justify-between items-start md:items-end gap-6 border-b border-slate-200 pb-10" in:fade>
    <div class="space-y-2">
      <div class="inline-flex items-center gap-2 px-3 py-1 rounded-full bg-sentinel-accent/5 border border-sentinel-accent/20 mb-2">
        <svg class="w-3 h-3 text-sentinel-accent" fill="none" stroke="currentColor" viewBox="0 0 24 24">
          <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M13 10V3L4 14h7v7l9-11h-7z" />
        </svg>
        <span class="text-xs font-semibold text-sentinel-accent">Health Insights Active</span>
      </div>
      <h1 class="text-4xl font-bold tracking-tight text-sentinel-text leading-tight">
        Health <span class="text-sentinel-accent italic font-bold">Insights</span>
      </h1>
      <div class="text-sm text-sentinel-dim font-medium">Analyze potential health risks based on your medical records.</div>
    </div>

    <button
      on:click={runPrediction}
      disabled={loading}
      class="hud-button hud-button-accent min-w-[200px]"
    >
      {#if loading}
        <div class="w-4 h-4 border-2 border-white/20 border-t-sentinel-optimal rounded-full animate-spin"></div>
        Analyzing records...
      {:else}
        <svg class="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
          <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M4 4v5h.582m15.356 2A8.001 8.001 0 004.582 9m0 0H9m11 11v-5h-.581m0 0a8.003 8.003 0 01-15.357-2m15.357 2H15" />
        </svg>
        Update Insights
      {/if}
    </button>
  </div>

  {#if errorMsg}
    <div class="hud-panel border-sentinel-critical/30 p-6 flex items-center gap-4 bg-sentinel-critical/5" in:fly={{ y: 10 }}>
       <svg class="w-6 h-6 hud-text-critical" fill="none" stroke="currentColor" viewBox="0 0 24 24">
          <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M12 9v2m0 4h.01m-6.938 4h13.856c1.54 0 2.502-1.667 1.732-3L13.732 4c-.77-1.333-2.694-1.333-3.464 0L3.34 16c-.77 1.333.192 3 1.732 3z" />
        </svg>
      <div class="text-[10px] hud-text-critical uppercase font-bold tracking-widest">{errorMsg}</div>
    </div>
  {/if}

  <PredictionsHUD {prediction} />
  
  <!-- Info Footer -->
  <div class="hud-panel p-6 bg-slate-50 border-slate-200 mt-10" in:fade={{ delay: 400 }}>
    <div class="flex gap-6 items-start">
      <div class="w-10 h-10 rounded-xl border border-slate-200 bg-white flex items-center justify-center shrink-0">
        <span class="text-sentinel-accent font-black text-lg">?</span>
      </div>
      <div class="space-y-1">
        <div class="text-xs font-bold text-sentinel-text">About Health Insights</div>
        <p class="text-xs text-sentinel-dim leading-relaxed">
          These insights are generated by our analysis engine based on your latest medical records. Results are probabilistic and intended for informational purposes only. Always consult a medical professional for clinical decisions.
        </p>
      </div>
    </div>
  </div>
</div>
